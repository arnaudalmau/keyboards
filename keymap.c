// This function is called to update the animation frames.
void animation_phase(void) {
    switch (anim_state) {
        case Idle:
            current_idle_frame = (current_idle_frame + 1) % IDLE_FRAMES;
            break;
        case Prep:
            // Prep has only 1 frame, no animation needed here
            break;
        case Tap:
            current_tap_frame = (current_tap_frame + 1) % TAP_FRAMES;
            break;
    }
}

