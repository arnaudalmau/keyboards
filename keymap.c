// This variable will track the number of currently pressed keys.
static uint8_t keys_pressed_count = 0;
// The duration of the grace period in milliseconds.
#define KEY_RELEASE_GRACE_PERIOD 200

// This function handles key presses and releases. It must return a boolean value.
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (record->event.pressed) {
        keys_pressed_count++;
    } else {
        keys_pressed_count--;
    }
    // Return true to allow the key event to be processed by other functions.
    return true;
}

// This function is responsible for drawing to both OLED screens.
bool oled_task_user(void) {
    // Get shared state that works on both halves
    led_t led_state = host_keyboard_led_state();
    bool fn_active = layer_state_is(_FUNC);
    uint8_t current_wpm = get_current_wpm();

    // Static variables for animation control
    static uint8_t local_keys_pressed_count = 0;
    static uint8_t local_anim_state = Idle;
    static uint32_t local_anim_timer = 0;
    static uint32_t local_grace_timer = 0;
    static uint8_t local_current_idle_frame = 0;
    static uint8_t local_current_tap_frame = 0;

    // Count the number of keys currently pressed
    local_keys_pressed_count = 0;
    for (uint8_t row = 0; row < MATRIX_ROWS; ++row) {
        for (uint8_t col = 0; col < MATRIX_COLS; ++col) {
            if (matrix_is_on(row, col)) {
                local_keys_pressed_count++;
            }
        }
    }

    // Animation state machine
    switch (local_anim_state) {
        case Idle:
            if (local_keys_pressed_count > 0) {
                local_anim_state = Tap;
                local_anim_timer = timer_read32();
                local_grace_timer = timer_read32();
            } else if (timer_elapsed32(local_anim_timer) > ANIM_FRAME_DURATION) {
                local_current_idle_frame = (local_current_idle_frame + 1) % IDLE_FRAMES;
                local_anim_timer = timer_read32();
            }
            break;
        case Tap:
            if (local_keys_pressed_count > 0) {
                local_grace_timer = timer_read32(); // Reset grace timer on new key press
            }
            if (timer_elapsed32(local_grace_timer) > 200) { // Grace period over
                if (local_keys_pressed_count == 0) {
                    local_anim_state = Prep;
                    local_anim_timer = timer_read32();
                }
            }
            // Always increment the tap frame if enough time has passed
            if (timer_elapsed32(local_anim_timer) > ANIM_FRAME_DURATION) {
                local_current_tap_frame = (local_current_tap_frame + 1) % TAP_FRAMES;
                local_anim_timer = timer_read32();
            }
            break;
        case Prep:
            if (timer_elapsed32(local_anim_timer) > 300) { // After 300ms in Prep, transition to Idle
                local_anim_state = Idle;
                local_anim_timer = timer_read32();
            }
            break;
    }

    // Render the animation based on the current state
    switch (local_anim_state) {
        case Idle:
            oled_write_raw_P(idle[local_current_idle_frame], ANIM_SIZE);
            break;
        case Prep:
            oled_write_raw_P(prep[0], ANIM_SIZE);
            break;
        case Tap:
            oled_write_raw_P(tap[local_current_tap_frame], ANIM_SIZE);
            break;
    }

    // Display WPM and other info
    oled_set_cursor(0, 0);
    char wpm_str[42];
    sprintf(wpm_str, "PPM:%03d", current_wpm);
    oled_write(wpm_str, false);

    oled_set_cursor(0, 1);
    oled_write_P(led_state.caps_lock ? PSTR("Bloq Maj") : PSTR(""), false);

    oled_set_cursor(0, 2);
    oled_write_P(fn_active ? PSTR("FN") : PSTR(""), false);

    return false;
}
